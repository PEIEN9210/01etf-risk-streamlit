def personal_score(row, theta):
    """
    å°‡ ETF Sharpe Ratio èˆ‡æŠ•è³‡äºº Î¸ çµåˆæˆå€‹äººåŒ–åˆ†æ•¸
    åˆ†æ•¸è¶Šé«˜ â†’ è¶Šç¬¦åˆæŠ•è³‡äººé¢¨éšªåå¥½
    """
    sharpe = row["Sharpe Ratio"]
    # åŸºæœ¬å…¬å¼: Î¸-model è¶Šæ¥è¿‘ ETF é¢¨éšªè¶Šå¥½
    type_risk = {"å‚µåˆ¸å‹":0.2,"é«˜è‚¡æ¯å‹":0.5,"è‚¡ç¥¨å‹":0.8}.get(row["å‹æ…‹"],0.6)
    # å°‡ Sharpe Ratio èˆ‡é¢¨éšªåŒ¹é…åº¦çµåˆ
    score = sharpe - abs(theta - type_risk)
    return score

